// Get all stocks as a flat array
export const getAllIndianStocks = () => {
    const allStocks = [];
    Object.values(INDIAN_STOCKS).forEach(category => {
        allStocks.push(...category);
    });
    // Remove duplicates
    return [...new Set(allStocks)];
};

// Get stocks by category
export const getStocksByCategory = (category) => {
    return INDIAN_STOCKS[category] || [];
};

// Search stocks by name/symbol
export const searchStocks = (query) => {
    const allStocks = getAllIndianStocks();
    const searchTerm = query.toUpperCase();
    return allStocks.filter(stock => 
        stock.includes(searchTerm) || 
        stock.replace('.NS', '').includes(searchTerm)
    );
};

